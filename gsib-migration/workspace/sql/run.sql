-- 5. Outstanding SGS (legacy ABA0024_OUTSTANDING_BOND).
--    bills_amount = T-Bills + CMTBs; sgs_md_amount = SGS (MD).
CREATE TABLE IF NOT EXISTS iss.iss_outstanding_sgs (
    id                 bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START 150000000000001),
    uuid               varchar(36)   DEFAULT gen_random_uuid(),
    year               varchar(4)    NOT NULL,
    month              varchar(2)    NOT NULL,
    bills_amount       numeric(13,0),
    sgs_md_amount      numeric(13,0),
    sgs_infra_amount   numeric(13,0),
    green_infra_amount numeric(13,0),
    version            numeric(5,0)  DEFAULT 1,
    is_deleted         varchar(1)    DEFAULT 'N',
    is_migrated        varchar(1)    DEFAULT 'Y',
    created_dt         timestamp     DEFAULT CURRENT_TIMESTAMP,
    created_by         varchar(36)   NOT NULL,
    updated_dt         timestamp     DEFAULT CURRENT_TIMESTAMP,
    updated_by         varchar(36)   NOT NULL,
    PRIMARY KEY (id),
    UNIQUE (uuid),
    UNIQUE (year, month)
);

-- 6. Outstanding MAS Bills (legacy ABA0026_OUTSTANDING_MAS).
