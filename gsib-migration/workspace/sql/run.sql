-- 1. Redemption run: master + audit
CREATE TABLE IF NOT EXISTS iss.iss_sb_redemption_run (
    id                    bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START 150000000000001),
    uuid                  varchar(36)   DEFAULT gen_random_uuid(),
    run_no                numeric(2,0)  NOT NULL,
    process_date          timestamp     NOT NULL,
    status                varchar(50)   NOT NULL,
    warning_error_details jsonb,
    total_amount_accepted numeric(13,0),
    total_amount_redeemed numeric(13,0),
    version               numeric(5,0)  DEFAULT 1,
    is_deleted            varchar(1)    DEFAULT 'N',
    is_migrated           varchar(1)    DEFAULT 'N',
    created_dt            timestamp     DEFAULT CURRENT_TIMESTAMP,
    created_by            varchar(36)   NOT NULL,
    updated_dt            timestamp     DEFAULT CURRENT_TIMESTAMP,
    updated_by            varchar(36)   NOT NULL,
    PRIMARY KEY (id),
    UNIQUE (uuid),
    UNIQUE (process_date, run_no)
);

CREATE TABLE IF NOT EXISTS iss.iss_sb_redemption_run_t (
    trans_id              bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START 150000000000001),
    trans_uuid            varchar(36)   DEFAULT gen_random_uuid(),
    id                    bigint        NOT NULL,
    uuid                  varchar(36)   NOT NULL REFERENCES iss.iss_sb_redemption_run(uuid),
    run_no                numeric(2,0)  NOT NULL,
    process_date          timestamp     NOT NULL,
    status                varchar(50)   NOT NULL,
    warning_error_details jsonb,
    total_amount_accepted numeric(13,0),
    total_amount_redeemed numeric(13,0),
    version               numeric(5,0)  NOT NULL,
    is_deleted            varchar(1)    NOT NULL,
    is_migrated           varchar(1)    NOT NULL,
    created_dt            timestamp     NOT NULL,
    created_by            varchar(36)   NOT NULL,
    updated_dt            timestamp     NOT NULL,
    updated_by            varchar(36)   NOT NULL,
    audit_action          varchar(1)    NOT NULL,
    audit_timestamp       timestamp     DEFAULT CURRENT_TIMESTAMP,
    is_history            varchar(1)    NOT NULL,
    PRIMARY KEY (trans_id),
    UNIQUE (trans_uuid)
);

-- 2. Redemption run summary: master + audit
CREATE TABLE IF NOT EXISTS iss.iss_sb_redemption_run_summary (
    id                         bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START 150000000000001),
    uuid                       varchar(36)   DEFAULT gen_random_uuid(),
    iss_sb_redemption_run_uuid varchar(36)   NOT NULL REFERENCES iss.iss_sb_redemption_run(uuid),
    iss_issuance_uuid          varchar(36)   NOT NULL,
    early_redemption_dt        timestamp     NOT NULL,
    total_redeemed_amount      numeric(13,0),
    total_outstanding_amount   numeric(13,0),
    version                    numeric(5,0)  DEFAULT 1,
    is_deleted                 varchar(1)    DEFAULT 'N',
    is_migrated                varchar(1)    DEFAULT 'N',
    created_dt                 timestamp     DEFAULT CURRENT_TIMESTAMP,
    created_by                 varchar(36)   NOT NULL,
    updated_dt                 timestamp     DEFAULT CURRENT_TIMESTAMP,
    updated_by                 varchar(36)   NOT NULL,
    PRIMARY KEY (id),
    UNIQUE (uuid),
    UNIQUE (iss_sb_redemption_run_uuid, iss_issuance_uuid)
);

CREATE TABLE IF NOT EXISTS iss.iss_sb_redemption_run_summary_t (
    trans_id                   bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START 150000000000001),
    trans_uuid                 varchar(36)   DEFAULT gen_random_uuid(),
    id                         bigint        NOT NULL,
    uuid                       varchar(36)   NOT NULL REFERENCES iss.iss_sb_redemption_run_summary(uuid),
    iss_sb_redemption_run_uuid varchar(36)   NOT NULL,
    iss_issuance_uuid          varchar(36)   NOT NULL,
    early_redemption_dt        timestamp     NOT NULL,
    total_redeemed_amount      numeric(13,0),
    total_outstanding_amount   numeric(13,0),
    version                    numeric(5,0)  NOT NULL,
    is_deleted                 varchar(1)    NOT NULL,
    is_migrated                varchar(1)    NOT NULL,
    created_dt                 timestamp     NOT NULL,
    created_by                 varchar(36)   NOT NULL,
    updated_dt                 timestamp     NOT NULL,
    updated_by                 varchar(36)   NOT NULL,
    audit_action               varchar(1)    NOT NULL,
    audit_timestamp            timestamp     DEFAULT CURRENT_TIMESTAMP,
    is_history                 varchar(1)    NOT NULL,
    PRIMARY KEY (trans_id),
    UNIQUE (trans_uuid)
);

